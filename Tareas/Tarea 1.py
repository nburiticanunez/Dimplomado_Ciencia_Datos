# Concepto de regresión lineal:
# La regresión lineal es una técnica estadística que busca modelar la relación entre una variable dependiente y una o más variables independientes. Se utiliza para predecir o estimar el valor de la variable dependiente en función de las variables independientes. El modelo de regresión lineal se representa mediante una ecuación de la forma:
# 
# y = β0 + β1x1 + β2x2 + ... + βnxn
# 
# Donde y es la variable dependiente, x1, x2, ..., xn son las variables independientes, β0, β1, β2, ..., βn son los coeficientes del modelo y representa la intersección con el eje y.
# 
# 1.5 Ejemplo:
# Supongamos que queremos estudiar la relación entre el número de horas de estudio y el rendimiento académico de los estudiantes. Tomamos una muestra de 50 estudiantes y registramos la cantidad de horas de estudio (x) y su puntaje en el examen (y). Aplicamos el modelo de regresión lineal y obtenemos la siguiente ecuación:
# 
# Rendimiento = 70 + 0.5 * Horas_de_estudio
# 
# Según esta ecuación, el coeficiente β0 es 70 y el coeficiente β1 es 0.5. Esto significa que por cada hora adicional de estudio, se espera un aumento de 0.5 puntos en el rendimiento académico.

